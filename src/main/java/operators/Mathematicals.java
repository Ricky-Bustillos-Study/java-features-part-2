package operators;

public class Mathematicals {

    public static void main(String[] args) {

        System.out.println(0 + 1); // 1
        System.out.println(3 - 1); // 2
        System.out.println(3 * 2); // 6
        System.out.println(8 % 2); // 0

        var number = 10;
        number *= 2; // 20

    }

}
